            About
            <p class="lead">About G-DASH</p>
        </h1>

        <div id="errordiv"></div>

        <div id="content">
            G-DASH is a lightweight, responsive, web-based user interface for Gulden users who run a wallet, node,
            witnessing
            account, or all of the above on a unix server (i.e. a Raspberry Pi). Using this dashboard users can keep an
            eye on
            their Gulden server and control their nodes and witnessing account without the need of a terminal. It also
            includes
            the option to control a Gulden wallet, and the software has auto-update functionality to make sure you
            always run
            the latest version of G-DASH.
            <br><br>
            This dashboard is created with the idea to keep everything as simple as possible. Of course, new ideas and
            comments
            are always welcome. You can find me on the Gulden slack (@bastijn) at
            <a href="https://guldencom.slack.com" target="_blank">https://guldencom.slack.com</a>, or via e-mail at
            <a href="mailto:bastijn@g-dash.nl">bastijn[at]g-dash.nl</a>. I'm happy to
            talk to you, to answer any questions and to listen to any feedback.
            <br><br>
            The website, development, repositories and the coffee are paid for by donations. So if you like this
            dashboard,
            please consider a donation to keep this website online and the development ongoing. Thank you!

